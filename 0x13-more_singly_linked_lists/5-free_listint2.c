/**
 * free_listint2 - Frees a linked list of type listint_t.
 * @head: A pointer to the head node of the list.
 */
void free_listint2(listint_t **head)
{
	listint_t *current, *next;

	if (head == NULL)
		return;

	current = *head;
	while (current != NULL)
	{
		next = current->next;
		free(current);
		current = next;
	}
	*head = NULL;
}
